package modelo;

public class Politico {

    private String nomePolitico, partidoPolitico, cargoPolitico, cidadePolitico, estadoPolitico, paisPolitico;
    private int idPolitico, numeroPolitico;

    public String getNomePolitico() {
        return nomePolitico;
    }

    public void setNomePolitico(String nomePolitico) {
        this.nomePolitico = nomePolitico;
    }

    public String getPartidoPolitico() {
        return partidoPolitico;
    }

    public void setPartidoPolitico(String partidoPolitico) {
        this.partidoPolitico = partidoPolitico;
    }

    public String getCargoPolitico() {
        return cargoPolitico;
    }

    public void setCargoPolitico(String cargoPolitico) {
        this.cargoPolitico = cargoPolitico;
    }

    public int getNumeroPolitico() {
        return numeroPolitico;
    }

    public void setNumeroPolitico(int numeroPolitico) {
        this.numeroPolitico = numeroPolitico;
    }

    public int getIdPolitico() {
        return idPolitico;
    }

    public void setIdPolitico(int idPolitico) {
        this.idPolitico = idPolitico;
    }

    public String getCidadePolitico() {
        return cidadePolitico;
    }

    public void setCidadePolitico(String cidadePolitico) {
        this.cidadePolitico = cidadePolitico;
    }

    public String getEstadoPolitico() {
        return estadoPolitico;
    }

    public void setEstadoPolitico(String estadoPolitico) {
        this.estadoPolitico = estadoPolitico;
    }

    public String getPaisPolitico() {
        return paisPolitico;
    }

    public void setPaisPolitico(String paisPolitico) {
        this.paisPolitico = paisPolitico;
    }

}
